	core:add_listener(
		"add_next_effect_to_bundle", 
		"ShortcutPressed",
		function(context) return context.string == "script_F4" end,
		function(context)
			if effect_test_vars.current_effect_to_add <= #effects_table_records then
				effects_test_apply_effect(effects_table_records[effect_test_vars.current_effect_to_add])
