		true
	)

	core:add_listener(
		"add_previous_effect_to_bundle", 
		"ShortcutPressed",
		function(context) return context.string == "script_F3" end,
		function(context) 
			effect_test_vars.current_effect_to_add = effect_test_vars.current_effect_to_add - 1
			if effect_test_vars.current_effect_to_add <= 1 then
				effect_test_vars.current_effect_to_add = 1
