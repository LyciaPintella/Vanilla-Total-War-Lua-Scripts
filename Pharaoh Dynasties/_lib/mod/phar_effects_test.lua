	core:add_listener(
		"add_next_bundle", 
		"ShortcutPressed",
		function(context) return context.string == "script_alt_F3" end,
		function(context) 
			if effect_test_vars.current_bundle_to_add <= #bundle_table_records then
				effects_test_apply_bundle(bundle_table_records[effect_test_vars.current_bundle_to_add])
				effect_test_vars.current_bundle_to_add = effect_test_vars.current_bundle_to_add + 1
			else
				out("<<<<<---- END OF BUNDLES TABLE REACHED. NOT APPLYING NEW EFFECTS ---->>>>>")
			end
		end, 
		true
	)

	core:add_listener(
		"add_previous_bundle", 
		"ShortcutPressed",
		function(context) return context.string == "script_alt_F2" end,
		function(context) 
			effect_test_vars.current_bundle_to_add = effect_test_vars.current_bundle_to_add - 1
			if effect_test_vars.current_bundle_to_add <= 1 then
				effect_test_vars.current_bundle_to_add = 1
			end
			effects_test_apply_bundle(bundle_table_records[effect_test_vars.current_bundle_to_add])
		end, 
		true
	)
	
