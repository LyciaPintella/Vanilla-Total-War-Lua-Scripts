				clear_effect_bundles(faction_key)
			end

		end, 
		true
	)
end

function get_bundles_for_faction(faction_key)
	local faction = cm:get_faction(faction_key)
	local bundles = faction:effect_bundles()
	return bundles
end

function clear_effect_bundles(faction_key)
	local faction_bundles = get_bundles_for_faction(faction_key)
	local bundle_num = faction_bundles:num_items()
	for i = 0, bundle_num - 1 do
		local curr_bundle_key = faction_bundles:item_at(i):key()
		cm:remove_effect_bundle(curr_bundle_key, faction_key)
	end

	faction_bundles = get_bundles_for_faction(faction_key)
	bundle_num = faction_bundles:num_items()
	if bundle_num >= 1 then 
		clear_effect_bundles(faction_key, faction_bundles)
	end
end

function effects_test_apply_effect(effect_key)
	local cust_effect_bundle_key_to_use = "phar_court_hittite_effect_bundle"
	local cust_effect_bundle = cm:create_new_custom_effect_bundle(cust_effect_bundle_key_to_use)
	cust_effect_bundle:add_effect(effect_key, "faction_to_faction_own", 10)
	cust_effect_bundle:set_duration(0)

	cm:apply_custom_effect_bundle_to_faction(cust_effect_bundle, cm:get_faction(cm:get_local_faction_name()))
	out("<<<<<---- To Reapply this EFFECT call this in the QA console: effects_test_apply_effect(" .. string.char(34) .. effect_key ..string.char(34)..") ----------->>>>")
	out("^^^^^---- Num:" .. effect_test_vars.current_effect_to_add)
end

