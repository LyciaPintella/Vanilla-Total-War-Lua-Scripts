--	Battle script override
--	Automatically loads a script in any battle launched from campaign. This activates advice in the battle.
function add_battle_script_override()
	cm:add_custom_battlefield(
		"generic",											-- string identifier
		0,													-- x co-ord
		0,													-- y co-ord
		5000,												-- radius around position
		false,												-- will campaign be dumped
		"",													-- loading override
		"script/battle/campaign_battle/battle_start.lua",	-- script override
		"",													-- entire battle override
		0,													-- human alliance when battle override
		false,												-- launch battle immediately
		true,												-- is land battle (only for launch battle immediately)
		false,												-- force application of autoresolver result
		false,												-- force a siege battle to be fought outside the settlement as a land battle
		false												-- disable fortification towers (for siege battles)
	);
end;


function remove_battle_script_override()
	cm:remove_custom_battlefield("generic");
end;



-- restrict confederation for duration of penalty
function start_confederation_listeners()
	core:add_listener(
		"confederation_listener",
		"FactionJoinsConfederation",
		true,
		function(context)
			local faction = context:confederation()
			local faction_name = faction:name()
			local faction_culture = faction:culture()
			local duration = 4
			if faction:is_human() then
				duration = 8
			elseif faction:name() == antagonist.current then 
				local player_faction = cm:get_faction(cm:get_local_faction_name(true))
				local player_strength = player_faction:strength()
				local antagonist_strength = faction:strength()

					if antagonist_strength/player_strength > 0.8 then
						duration = 99
					end
			end

			out("Restricting confederation between [faction:" .. faction_name .. "] and [culture:" .. faction_culture .. "]")
			cm:force_diplomacy("faction:" .. faction_name, "culture:" .. faction_culture, "form confederation", false, true, false)
				
			-- Confederation is possible trough force_confederation() and it is possible to have multiple "ScriptEventConfederationExpired" events for a single faction. This way, it's only one at a time.

			if is_table(cm.turn_countdown_events[faction_name]) then 
				
				for i = 1, #cm.turn_countdown_events[faction_name] do
					if cm.turn_countdown_events[faction_name][i].event_name == "ScriptEventConfederationExpired" then
						table.remove(cm.turn_countdown_events[faction_name], i);
					end;
				end;
			
				cm:add_turn_countdown_event(faction_name, duration, "ScriptEventConfederationExpired", faction_name)
			else
				cm:add_turn_countdown_event(faction_name, duration, "ScriptEventConfederationExpired", faction_name)
			end
		end,
		true
	)

	core:add_listener(
		"confederation_expired",
		"ScriptEventConfederationExpired",
		true,
		function(context)
			local faction_name = context.string
			local faction = cm:get_faction(faction_name)
			local culture = faction:culture()

			out("Unrestricting confederation between [faction:" .. faction_name .. "] and [culture: " .. culture .. "]")
			cm:force_diplomacy("faction:" .. faction_name, "culture:" .. culture, "form confederation", true, true, false)

			--Another one of them special cases
			if antagonist.current ~= "" then 
				cm:force_diplomacy("faction:" .. faction_name, "faction:" .. antagonist.current, "form confederation", false, true, false)
			end
		end,
		true
	)
end

-- increase upkeep for each additional army the player recruits or obtains via confederation
function player_army_count_listener()

	-- apply the effect bundles every time the player turn starts
	core:add_listener(
		"player_army_turn_start_listener",
		"FactionTurnStart",
		function(context)
			return upkeep_penalty_condition(context:faction())
		end,
		function(context)
			apply_upkeep_penalty(context:faction())
		end,
		true
	)

	-- apply the effect bundles every time the player creates a new force
	core:add_listener(
		"player_army_created_listener",
		"MilitaryForceCreated",
		function(context)
			return upkeep_penalty_condition(context:military_force_created():faction())
		end,
		function(context)
			apply_upkeep_penalty(context:military_force_created():faction())
		end,
		true
	)

	-- apply the effect bundles every time the player confederates
	core:add_listener(
		"confederation_player_army_count_listener",
		"FactionJoinsConfederation",
		function(context)
			return upkeep_penalty_condition(context:confederation())
		end,
		function(context)
			apply_upkeep_penalty(context:confederation())
		end,
		true
	)
end

function upkeep_penalty_condition(faction)
	return faction:is_human() and faction:is_allowed_to_capture_territory()
end

-- loop through the player's armys and apply
function apply_upkeep_penalty(faction)
	local difficulty = cm:model():combined_difficulty_level()

	local effect_bundle = "effect_bundle_supply_lines_easy"

	if difficulty == 0 then
		effect_bundle = "effect_bundle_supply_lines_normal"
	elseif difficulty == -1 then
		effect_bundle = "effect_bundle_supply_lines_hard"
	elseif difficulty == -2 then
		effect_bundle = "effect_bundle_supply_lines_very_hard"
	elseif difficulty == -3 then 
		effect_bundle = "effect_bundle_supply_lines_legendary"
	end
	
	local mf_list = faction:military_force_list()
	local num_items = mf_list:num_items()

	-- go through every general the player has, ignoring the first, and apply the effect bundles
	for i = 1, mf_list:num_items() - 1 do
		current_mf = mf_list:item_at(i)

		if not current_mf:is_armed_citizenry() and not current_mf:has_effect_bundle(effect_bundle) and current_mf:has_general() then
			--out.design("Adding supply line bundle to Military force with CQI : " .. tostring(current_mf:command_queue_index()))
			cm:apply_effect_bundle_to_characters_force(effect_bundle, current_mf:general_character():command_queue_index(), 0)
		end
	end
end


function setup_rebel_army_siege_equipment_monitor()
	core:add_listener(
		"rebel_army_monitor",
		"FactionTurnStart",
		function(context) return context:faction():name() == "rebels" end,
		function(context)
			local char_list = context:faction():character_list();
			for i = 0, char_list:num_items() - 1 do
				current_char = char_list:item_at(i)
				if current_char:has_military_force() then
					local cqi = current_char:military_force():command_queue_index()
					cm:apply_effect_bundle_to_force("effect_bundle_force_siege_equipment_rebels", cqi, 0)
				end
			end
		end,
		true
	);
end

