function start_story_dilemma_listeners()

	core:add_listener(
		"troy_story_dilemma_initial_trigger",
		"FactionTurnStart",
		function(context)
			local faction = context:faction()
			if not faction:is_human() or cm:model():turn_number() > story_dilemma_max_trigger_turn then
				return false
			else
				local effect_bundles = faction:effect_bundles()
				for i=0, effect_bundles:num_items() -1 do
					local current_key = effect_bundles:item_at(i):key()
					if  string.find(current_key, "effect_bundle_strategic_threat") and
						(string.find(current_key, "_3") or string.find(current_key, "_4") or string.find(current_key, "_5") or string.find(current_key, "_6") or string.find(current_key, "_7"))
					then
						return true
					end
				end
			end

		end,
		function(context)

			if story_dilemma_initial_trigger_done then
				return
			end

			local faction = context:faction()
			local factions_at_war_with = faction:factions_at_war_with() 
			local player_culture = faction:culture()
			if player_culture == "troy_ama_clt_hordes" or player_culture == "troy_rem_clt_thraco_phrygians" or player_culture == "troy_rem_clt_hordes_aethiopians" then
				player_culture = "troy_main_clt_trojans"
			end

			for i = 0, factions_at_war_with:num_items() -1 do
				if 	factions_at_war_with:item_at(i):culture() == player_culture and
					(factions_at_war_with:item_at(i):is_major() or story_dilemma_faithful_ally_factions[player_culture][factions_at_war_with:item_at(i):name()])
				then
					return
				end
			end

			story_dilemma_initial_trigger_done = true
			cm:trigger_dilemma(faction:name(), story_dilemma_initial_key[player_culture])
		end,
		false
	)

	core:add_listener(
		"troy_story_dilemma_choice_third",
		"DilemmaChoiceMadeEvent",
		function(context)
			return (context:dilemma() == story_dilemma_initial_key[context:faction():culture()]) and story_dilemma_initial_trigger_done and (context:choice() == 2)
