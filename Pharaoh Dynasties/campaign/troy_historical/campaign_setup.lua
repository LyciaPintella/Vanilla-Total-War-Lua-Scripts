function trigger_steal_the_palladium_mission(faction, troy_region)
	local mission_key = "troy_adi_mission_stealing_the_palladium"
	local payload_1 = "text_display {  lookup dummy_troy_adi_palladium_troy_debuff;}"
	local payload_2 = "text_display { lookup dummy_troy_adi_palladium_ancillary_payload; }"

	local paladium_ancillary_key = "troy_adi_anc_item_palladium"
	local troy_debuff_bundle_key = "troy_adi_effect_bundle_palladium_stolen"
	
	local MOVE_FL_TO_TROY = "mission_text_text_troy_adi_scripted_mission_move_faction_leader_to_troy"

	local mission = mission_manager:new(faction:name(), mission_key,  function()
		cm:add_ancillary_to_faction(faction:name(), paladium_ancillary_key, true)
		cm:apply_effect_bundle_to_region(troy_debuff_bundle_key, troy_region:name(), -1)
		troy_adi_palladium_vars.palladium_stolen_by_faction = faction:name()
		troy_adi_palladium_vars.palladium_stolen = true
	end )
	
	out("--------> Spawning mission (" .. mission_key .. ") for faction: (" .. faction:name() .. ")")

	mission:add_new_scripted_objective(
		MOVE_FL_TO_TROY,
		"CharacterFinishedMovingEvent",
		function(context)
			local character = context:character()
			if character:faction():name() == faction:name() and character:is_faction_leader() then 
				if character:region() == troy_region then 
					return true
				end
			end
		end,
		"ScriptedObjectiveStealPalladiumMoveFactionLeader",
		true,
		true
	)
	-- abort the mission if the player has captured Troy.
	mission:add_scripted_objective_failure_condition(
		"RegionFactionChangeEvent",
		function(context)
			local context_region = context:region()
			return context_region == troy_region and context_region:owning_faction() == faction
		end
	)

	mission:set_mission_issuer("CLAN_ELDERS")
	mission:add_payload(payload_1)
	mission:add_new_objective("MOVE_TO_REGION", true)
	mission:add_condition("region " .. troy_region:name())
	mission:add_condition("agent_subtype troy_agent_spy")
	mission:add_payload(payload_2)
	mission:set_should_cancel_before_issuing(false)
	if not mission.is_registered then 
		mission:register()
	end

	core:remove_listener("troy_adi_stealing_the_palladium_mission_trigger_listener")
	troy_adi_palladium_vars.stealing_the_palldium_triggered = true

	return mission, mission_key
	
end

function stealing_the_palladium_listeners()
	local local_faction = cm:get_faction(cm:get_local_faction_name(true))
	local troy_region = cm:get_region("troy_main_ilion_troy")
	if not troy_adi_palladium_vars.stealing_the_palldium_triggered then 
		core:add_listener(
			"troy_adi_stealing_the_palladium_mission_trigger_listener",
			"FactionTurnStart",
			function(context)
				local faction = context:faction()
				local char_list = faction:character_list()
				local troy_faction = cm:get_faction("troy_main_trj_troy")
				local zoleia_region = cm:get_region("troy_main_ide_zeleia")

				if troy_faction and zoleia_region and not troy_faction:is_null_interface() and not troy_faction:is_dead() then 
					if faction:is_human() and faction:culture() == "troy_main_clt_danaans" and faction:at_war_with(troy_faction) then 
						for i = 0, char_list:num_items() - 1 do
							local curr_char = char_list:item_at(i)
							if curr_char:has_region() and curr_char:character_type("general") and curr_char:region() == zoleia_region then 
								return true
							end
						end
					end
				end
			end,

			function(context)
				local faction = context:faction()
				local mission, mission_key = trigger_steal_the_palladium_mission(faction, troy_region)
				local x = troy_region:settlement():logical_position_x()
				local y = troy_region:settlement():logical_position_y()

				mission:trigger()
				cm:set_scripted_mission_position(mission_key, "ScriptedObjectiveStealPalladiumMoveFactionLeader", x, y)
			end,
			true
		)
	else
		trigger_steal_the_palladium_mission(local_faction, troy_region)
	end
end

core:add_listener(
	"troy_adi_stealing_the_palladium_troy_debuff",
	"RegionFactionChangeEvent",
	function(context)
		local region = context:region()
		return troy_adi_palladium_vars.palladium_stolen and region:name() == troy_adi_palladium_vars.troy_region_key and region:owning_faction():name() ~= troy_adi_palladium_vars.palladium_stolen_by_faction
	end,

	function(context)
		cm:apply_effect_bundle_to_region(troy_adi_palladium_vars.palladium_stolen_effect_bundle, troy_adi_palladium_vars.troy_region_key, -1)
	end,
	true
)

cm:add_saving_game_callback(
	function(context)
		cm:save_named_value("STEALING_THE_PALLADIUM_TRIGGER", troy_adi_palladium_vars, context)
	end
)

cm:add_loading_game_callback(
	function(context)
		troy_adi_palladium_vars = cm:load_named_value("STEALING_THE_PALLADIUM_TRIGGER", troy_adi_palladium_vars, context)
	end
)