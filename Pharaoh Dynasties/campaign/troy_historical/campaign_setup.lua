core:add_listener(
	"FirstTickAfterWorldCreated_mythological_unit_icons",
	"FirstTickAfterWorldCreated",
	true,
	function(context)
		local faction_name = cm:get_local_faction_name(true)
		if faction_name then 
			if not cm:get_faction(faction_name):is_allowed_to_capture_territory() then
				return -- Hordes do not see the mythological unit icons b/c they cannot build the correspondng buildings. They can only receive the units as capture bonuses.
			elseif cm:get_campaign_name() == "troy_historical" then
				return -- These icons should not appear in the historical campaign
			end
		end

		local region_list = context:world():region_manager():region_list();
		

		for i = 0, region_list:num_items() - 1 do
			local region = region_list:item_at(i);
			local settlement = region:settlement()
			local slot_list = region:slot_list()
			local region_name = region:name()
			
			if faction_name == "troy_main_dan_ithaca" and not settlement:is_port() then 
				for k = 0, slot_list:num_items() - 1 do
					local slot = slot_list:item_at(k)
					if slot:type() == "primary" then
						for b_set, b_details in pairs(mythical_unit_icons_for_odysseus) do
							if string.find(slot:template_key(), "ody_settlement") and string.find(slot:template_key(), b_set) then
								cm:set_custom_settlement_info_icon(region_name, "icon_" .. b_set, b_details[1], b_details[2])
								cm:set_custom_settlement_info_icon_visibility(region_name, "icon_" .. b_set, true)
							end
						end	
					end
				end
			else
				for set, details in pairs(mythological_unit_sets) do
					if settlement:can_possibly_build_units_from_set(set) then
						cm:set_custom_settlement_info_icon(region_name, "icon_" .. set, details[1], details[2])
						cm:set_custom_settlement_info_icon_visibility(region_name, "icon_" .. set, true)
					end
				end
				for k = 0, slot_list:num_items() - 1 do
					local slot = slot_list:item_at(k)
					for b_set, b_details in pairs(mythological_building_slot) do
						if slot:template_key() == b_set then
							cm:set_custom_settlement_info_icon(region_name, "icon_" .. b_details[1], b_details[2], b_details[3])
							cm:set_custom_settlement_info_icon_visibility(region_name, "icon_" .. b_details[1], true)
						end
					end	
				end
			end
		end
	end
)

core:add_listener(
	"RegionFactionChangeEvent_access_to_mythological_units",
	"RegionFactionChangeEvent",
	function(context)
		local reg = context:region()
		return reg:owning_faction():is_human()
	end,
	function(context)
		local region = context:region()
		local settlement = region:settlement()
		local faction_name = region:owning_faction():name()

		if faction_name == "troy_main_dan_ithaca" and not settlement:is_port() then 
			local slot_list = region:slot_list()
			for k = 0, slot_list:num_items() - 1 do
				local slot = slot_list:item_at(k)
				if slot:type() == "primary" then
					for b_set, b_details in pairs(mythical_unit_icons_for_odysseus) do
						if string.find(slot:template_key(), "ody_settlement") and string.find(slot:template_key(), b_set) and not (cm:get_campaign_name() == "troy_historical") then
							local title = "event_feed_strings_text_troy_main_event_feed_myth_units_unlock_title"
							local pic = 1040
							local primary_detail = "regions_onscreen_" .. region:name()
							local secondary_detail = b_details[3]

							cm:show_message_event(
								faction_name,
								title,
								primary_detail,
								secondary_detail,
								true,
								pic,
								end_callback,
								delay,
								false
							)
						end
					end	
				end
			end
		else
			for set, details in pairs(mythological_unit_sets) do
				if settlement:can_possibly_build_units_from_set(set) and not (cm:get_campaign_name() == "troy_historical") then
					local title = "event_feed_strings_text_troy_main_event_feed_myth_units_unlock_title"
					local pic = 1040
					local primary_detail = "regions_onscreen_" .. region:name()
					local secondary_detail = details[3]

					cm:show_message_event(
						faction_name,
						title,
						primary_detail,
						secondary_detail,
						true,
						pic,
						end_callback,
						delay,
						false
					)
				end
			end
		end
	end,
	true
)

