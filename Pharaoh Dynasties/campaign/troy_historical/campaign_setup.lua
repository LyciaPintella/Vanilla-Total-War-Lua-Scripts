		function(context)
			local faction = context:faction()
			local player_culture = faction:culture()
			if player_culture == "troy_ama_clt_hordes" or player_culture == "troy_rem_clt_thraco_phrygians" or player_culture == "troy_rem_clt_hordes_aethiopians" then
				player_culture = "troy_main_clt_trojans"
			end
			local factions_list = cm:model():world():faction_list()
			for i=0, factions_list:num_items() - 1 do
				local curr_faction = factions_list:item_at(i)
				if curr_faction:culture() == player_culture and 
					(curr_faction:is_major() or story_dilemma_faithful_ally_factions[player_culture][curr_faction:name()])
				then
					cm:cai_strategic_stance_manager_block_all_stances_but_that_specified_towards_target_faction(
						curr_faction:name(),
						faction:name(),
						"CAI_STRATEGIC_STANCE_BEST_FRIENDS"
					)
				elseif curr_faction:culture() ~= player_culture and curr_faction:is_major() then
					local same_cult_factions = curr_faction:factions_of_same_culture()
					for j=0, same_cult_factions:num_items() -1 do
						local curr_cult_faction = same_cult_factions:item_at(j)
						if curr_cult_faction:is_major() then
							cm:cai_strategic_stance_manager_block_all_stances_but_that_specified_towards_target_faction(
								curr_faction:name(),
								curr_cult_faction:name(),
								"CAI_STRATEGIC_STANCE_BEST_FRIENDS"
							)
						end
					end
				end
			end
			story_dilemma_player_chose_story = true
		end,
		false
	)

