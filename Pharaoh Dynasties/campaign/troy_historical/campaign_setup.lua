	"Troy_city_garrison_replenishment",
	"BattleCompleted",
	true,
	function(context)
		local battle = context:pending_battle()

		if not (battle:has_region() and battle:region():name() == troy_city_region_key) then
			return
		end

		local secondary_attackers = battle:secondary_attackers()
		for i = 0, secondary_attackers:num_items() - 1 do
			local char = secondary_attackers:item_at(i)
			if not char:is_null_interface() and char:has_military_force() then
				local military_force = char:military_force()
				if military_force:is_armed_citizenry() then
					cm:replenish_units(cm:char_lookup_str(char:command_queue_index()), troy_city_post_battle_replenishment)
					return
				end
			end
		end

		local secondary_defenders = battle:secondary_defenders()
		for i = 0, secondary_defenders:num_items() - 1 do
			local char = secondary_defenders:item_at(i)
			if not char:is_null_interface() and char:has_military_force() then
				local military_force = char:military_force()
				if military_force:is_armed_citizenry() then
					cm:replenish_units(cm:char_lookup_str(char:command_queue_index()), troy_city_post_battle_replenishment)
					return
				end
			end
		end

		local attacker = battle:attacker()
		if battle:has_attacker() and not attacker:is_null_interface() then
			local attacker_military_force = attacker:military_force()
			if attacker_military_force:is_armed_citizenry() then
				cm:replenish_units(cm:char_lookup_str(attacker:command_queue_index()), troy_city_post_battle_replenishment)
				return
			end
		end

		local defender = battle:defender()
		if battle:has_defender() and not defender:is_null_interface() then
			local defender_military_force = defender:military_force()
			if defender_military_force:is_armed_citizenry() then
				cm:replenish_units(cm:char_lookup_str(defender:command_queue_index()), troy_city_post_battle_replenishment)
				return
			end
		end

	end,
	true
)

