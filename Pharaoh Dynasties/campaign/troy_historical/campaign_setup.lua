		"troy_story_dilemma_attacking_faithful_ally",
		"NegativeDiplomaticEvent",
		function(context)
			local faction = context:character():faction()
			return story_dilemma_player_chose_story and faction:is_human() and faction == context:proposer() and context:is_war()
