function start_player_bankrupt_listeners()
	core:add_listener(
		"Player_Bankrupt",
		"PooledResourceShortageEvent",
		function(context)
			return context:faction():is_human()
		end,
		function(context)
			local faction = context:faction()
			local resource = context:resource()
			local resource_key = resource:key()
			local num_turns = resource:turns_of_shortage()
			local bundle
			if player_bankrupt_bundles[resource_key] and player_bankrupt_bundles[resource_key][num_turns] then
				bundle = player_bankrupt_bundles[resource_key][num_turns]
			elseif player_bankrupt_bundles[resource_key] and num_turns > #player_bankrupt_bundles[resource_key] then
				bundle = player_bankrupt_bundles[resource_key][#player_bankrupt_bundles[resource_key]]
			end
			out(bundle)
			if faction:has_effect_bundle(bundle) then
				return
			else
				cm:apply_effect_bundle(bundle, faction:name(), -1)
			end
		end,
		true
	)
	core:add_listener(
		"Player_Bankruptcy_Ended",
		"PooledResourceShortageEndedEvent",
		function(context)
			return context:faction():is_human()
		end,
		function(context)
			local faction = context:faction()
			local resource = context:resource()
			local resource_key = resource:key()
			local bundles_to_remove = player_bankrupt_bundles[resource_key]
			if is_table(bundles_to_remove) then
				for k,v in pairs(bundles_to_remove) do
					if faction:has_effect_bundle(v) then
						cm:remove_effect_bundle(v,faction:name())
					end
				end
			end
		end,
		true
	)
end


