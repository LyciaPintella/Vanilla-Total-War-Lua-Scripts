	-- store a flag per-campaign for the player razing
	if not cm:get_faction(local_faction):is_allowed_to_capture_territory() and not cm:get_saved_value("player_has_razed") then
		core:add_listener(
			"player_has_razed",
			"CharacterRazedSettlement",
			function(context)
				return context:character():faction():name() == local_faction;
			end,
			function()
				cm:set_saved_value("player_has_razed", true);
			end,
			false
		);
	end;

	-- apply debuff to sovereign after successful acquiring of subject
	core:add_listener(
		"FactionBecomesSubject_Bundle_Sovereign",
		"FactionBecomesSubject",
		function(context) return context:faction():get_sovereign():is_human() end,
		function(context)
			local player_fac = context:faction():get_sovereign()
			local effect_bundles = player_fac:effect_bundles()
			for i=0, effect_bundles:num_items() -1 do
				local custom_bundle = cm:clone_or_create_custom_effect_bundle(player_fac:name(), "phar_main_sovereign_debuff_after_vassalization")
				local current_key = effect_bundles:item_at(i):key()
				if  string.find(current_key, "effect_bundle_strategic_threat") then
					local threat_bundle_level = string.sub(current_key, string.len(current_key), -1)
					local debuff_step = -10
					custom_bundle:set_duration(6)
					custom_bundle:add_effect("phar_main_evaluation_add_subjugation","faction_to_faction_own_unseen",(threat_bundle_level * debuff_step))	
					return cm:apply_custom_effect_bundle_to_faction(custom_bundle, player_fac)
				end
			end
		end,
		true
	);
end;

function is_tutorial_mode()
	if not cm:is_game_loaded() then
		script_error("WARNING: is_tutorial_mode() called too early, the game has not yet been created. Returning false, but this may be the wrong value")
		return false
	end
	
	return core:svr_load_bool("sbool_player_selected_first_turn_intro_on_frontend") or cm:get_saved_value("bool_is_tutorial_mode")
end

--	Battle script override
--	Automatically loads a script in any battle launched from campaign. This activates advice in the battle.
function add_battle_script_override()
	cm:add_custom_battlefield(
		"generic",											-- string identifier
		0,													-- x co-ord
		0,													-- y co-ord
		5000,												-- radius around position
		false,												-- will campaign be dumped
		"",													-- loading override
		"script/battle/campaign_battle/battle_start.lua",	-- script override
		"",													-- entire battle override
		0,													-- human alliance when battle override
		false,												-- launch battle immediately
		true,												-- is land battle (only for launch battle immediately)
		false,												-- force application of autoresolver result
		false,												-- force a siege battle to be fought outside the settlement as a land battle
		false												-- disable fortification towers (for siege battles)
	);
end;

function remove_battle_script_override()
	cm:remove_custom_battlefield("generic");
end;

-- restrict confederation for duration of penalty
function start_confederation_listeners()
	core:add_listener(
		"confederation_listener",
		"FactionJoinsConfederation",
		true,
		function(context)
			local faction = context:confederation()
			local faction_name = faction:name()
			local faction_culture = faction:culture()
			local duration = cm:model():get_campaign_variable_by_name(faction:is_human() and "confederation_cooldown_per_culture_player" or "confederation_cooldown_per_culture_ai")

			out("Restricting confederation between [faction:" .. faction_name .. "] and [culture:" .. faction_culture .. "]")
			cm:force_diplomacy("faction:" .. faction_name, "culture:" .. faction_culture, "form confederation", false, true, false)
				
			-- Confederation is possible trough force_confederation() and it is possible to have multiple "ScriptEventConfederationExpired" events for a single faction. This way, it's only one at a time.

			if is_table(cm.turn_countdown_events[faction_name]) then 
				
				for i = 1, #cm.turn_countdown_events[faction_name] do
					if cm.turn_countdown_events[faction_name][i].event_name == "ScriptEventConfederationExpired" then
						table.remove(cm.turn_countdown_events[faction_name], i);
					end;
				end;
			
				cm:add_turn_countdown_event(faction_name, duration, "ScriptEventConfederationExpired", faction_name)
			else
				cm:add_turn_countdown_event(faction_name, duration, "ScriptEventConfederationExpired", faction_name)
			end
		end,
		true
	)

	core:add_listener(
		"confederation_expired",
		"ScriptEventConfederationExpired",
		true,
		function(context)
			local faction_name = context.string
			local faction = cm:get_faction(faction_name)
			local culture = faction:culture()

			out("Unrestricting confederation between [faction:" .. faction_name .. "] and [culture: " .. culture .. "]")
			cm:force_diplomacy("faction:" .. faction_name, "culture:" .. culture, "form confederation", true, true, false)
		end,
		true
	)
end

function setup_rebel_army_siege_equipment_monitor()
	core:add_listener(
		"rebel_army_monitor",
		"FactionTurnStart",
		function(context) return context:faction():name() == "rebels" end,
		function(context)
			local char_list = context:faction():character_list();
			for i = 0, char_list:num_items() - 1 do
				current_char = char_list:item_at(i)
				if current_char:has_military_force() then
					local cqi = current_char:military_force():command_queue_index()
					cm:apply_effect_bundle_to_force("effect_bundle_force_siege_equipment_rebels", cqi, 0)
				end
			end
		end,
		true
	);
end

