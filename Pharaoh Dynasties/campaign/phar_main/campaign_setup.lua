		"appoint_new_general_listener",
		"PanelOpenedCampaign",
		function(context)
			return context.string == "appoint_new_general";
		end,
		function()
			-- a hack: we trigger this event after a short delay as the panel hasn't been fully built when we get the PanelOpenedCampaign event
			cm:callback(function() core:trigger_event("ScriptEventAppointNewGeneralPanelOpened") end, 0.5);
