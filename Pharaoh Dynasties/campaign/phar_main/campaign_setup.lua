	local primary_detail = "factions_screen_name_" .. faction_name
	local faction_to_secondary_detail_and_pic = {
		phar_main_tausret = 	{ secondary_detail = "event_feed_strings_text_troy_main_event_feed_string_scripted_event_intro_phthia_secondary_detail", pic = 1018 },
		phar_main_kurunta = 	{ secondary_detail = "event_feed_strings_text_troy_main_event_feed_string_scripted_event_intro_phthia_secondary_detail", pic = 1018 },
		phar_main_ramesses = 	{ secondary_detail = "event_feed_strings_text_troy_main_event_feed_string_scripted_event_intro_phthia_secondary_detail", pic = 1018 },
		phar_main_seti = 		{ secondary_detail = "event_feed_strings_text_troy_main_event_feed_string_scripted_event_intro_phthia_secondary_detail", pic = 1018 },
		phar_main_suppiluliuma ={ secondary_detail = "event_feed_strings_text_troy_main_event_feed_string_scripted_event_intro_phthia_secondary_detail", pic = 1018 },
		phar_main_amenmesse = 	{ secondary_detail = "event_feed_strings_text_troy_main_event_feed_string_scripted_event_intro_phthia_secondary_detail", pic = 1018 },
		phar_main_mefka_qes = 	{ secondary_detail = "event_feed_strings_text_troy_main_event_feed_string_scripted_event_intro_phthia_secondary_detail", pic = 1018 },
		phar_main_irsu = 		{ secondary_detail = "event_feed_strings_text_troy_main_event_feed_string_scripted_event_intro_phthia_secondary_detail", pic = 1018 },
