function spawn_start_pos_army_to_beat(faction)
	local faction_name = faction:name()
	local campaign_name = cm:get_campaign_name()
	out("FACTION NAME " .. faction_name)
	if start_pos_armies_to_beat[faction_name] and start_pos_armies_to_beat[faction_name].subtype and start_pos_armies_to_beat[faction_name].forename then
		cm:create_force_with_general(
			start_pos_armies_to_beat[faction_name].faction,
			start_pos_armies_to_beat[faction_name].unit_list[campaign_name],
			start_pos_armies_to_beat[faction_name].region,
			start_pos_armies_to_beat[faction_name].x_coordinate,
			start_pos_armies_to_beat[faction_name].y_coordinate,
			"general",
			start_pos_armies_to_beat[faction_name].subtype,
			start_pos_armies_to_beat[faction_name].forename,
			"",
			"",
			"",
			false,
			function(cqi)
				if cqi then 
					local character = cm:get_character_by_cqi(cqi)
					cm:disable_movement_for_character("character_cqi:" .. tostring(cqi))
					if character and not character:is_null_interface() then
						cm:set_force_has_retreated_this_turn(character)

						if start_pos_armies_to_beat[faction_name].traits then
							for _, trait_key in ipairs(start_pos_armies_to_beat[faction_name].traits) do
								cm:force_add_trait("character_cqi:" .. tostring(cqi), trait_key, false, 1)
							end
						end

						if start_pos_armies_to_beat[faction_name].ancillaries then
							for _, ancillary_key in ipairs(start_pos_armies_to_beat[faction_name].ancillaries) do
								cm:force_add_ancillary("character_cqi:" .. tostring(cqi), ancillary_key, true)
							end
						end
					end
					start_pos_armies_to_beat[faction_name].cqi = cqi
					core:add_listener(
						"enable_movement_start_pos_army", 
						"FactionTurnEnd", 
						function(context)
							return context:faction():name() == faction_name and (cm:model():turn_number() == 1)
						end, 
						function(context)
							cm:enable_movement_for_character("character_cqi:" .. tostring(cqi))
						end, 
						false
					)
				end 
			end
		)
	else

		if start_pos_armies_to_beat[faction_name] then
			cm:create_force(
				start_pos_armies_to_beat[faction_name].faction,
				start_pos_armies_to_beat[faction_name].unit_list[campaign_name],
				start_pos_armies_to_beat[faction_name].region,
				start_pos_armies_to_beat[faction_name].x_coordinate,
				start_pos_armies_to_beat[faction_name].y_coordinate,
				true,
				function(cqi)
					if cqi then 
						local character = cm:get_character_by_cqi(cqi)
						cm:disable_movement_for_character("character_cqi:" .. tostring(cqi))
						if character and not character:is_null_interface() then
							cm:set_force_has_retreated_this_turn(character)
							
							if start_pos_armies_to_beat[faction_name].traits then
								for _, trait_key in ipairs(start_pos_armies_to_beat[faction_name].traits) do
									cm:force_add_trait("character_cqi:" .. tostring(cqi), trait_key, false, 1)
								end
							end
							
							if start_pos_armies_to_beat[faction_name].ancillaries then
								for _, ancillary_key in ipairs(start_pos_armies_to_beat[faction_name].ancillaries) do
									cm:force_add_ancillary("character_cqi:" .. tostring(cqi), ancillary_key, true)
								end
							end
						end
						start_pos_armies_to_beat[faction_name].cqi = cqi
						core:add_listener(
							"enable_movement_start_pos_army", 
							"FactionTurnEnd", 
							function(context)
								return context:faction():name() == faction_name and (cm:model():turn_number() == 1)
							end, 
							function(context)
								cm:enable_movement_for_character("character_cqi:" .. tostring(cqi))
							end, 
							false
						)
					end 
				end,
				false
