function spawn_historical_characters(faction)
	local faction_name = faction:name()
	local faction_historical_characters = historical_characters[faction_name]
	if faction_historical_characters then
		local current_turn = cm:model():turn_number()
		for _, c in ipairs(faction_historical_characters) do
			if not c.used and (current_turn >= c.spawning_window_start_turn) and (current_turn <= c.spawning_window_end_turn) then
				if cm:model():random_percent(c.spawn_chance_percent) then
					cm:spawn_character_to_pool(faction_name, c.name_key, c.surname_key, c.clan_name_key, c.other_name_key, c.age, c.is_male, c.agent_type, c.agent_subtype, c.is_immortal, c.art_set_id, c.character_trait_levels, c.loyalty_traits)
					c.used = true	-- Prevent this character from entering the pool a second time.
				end
			end
		end
	end
end

core:add_listener(
	"spawn_historical_characters_round_start",
	"RoundStart",
	true,
	function(context)
		local faction_list = context:model():world():faction_list()
		for i = 0, faction_list:num_items() - 1 do
			spawn_historical_characters(faction_list:item_at(i))
		end
	end,
	false
)

