				if c.used then
					if not characters_used[k] then
						characters_used[k] = {}
					end
					table.insert(characters_used[k], i)
				end
			end
		end
		cm:save_named_value("troy_historical_characters_used", characters_used, context)
	end
)

cm:add_loading_game_callback(
	function(context)
		local characters_used = {}
		characters_used = cm:load_named_value("troy_historical_characters_used", characters_used, context)
		for k, v in pairs(characters_used) do
			for _, i in ipairs(v) do
				if historical_characters[k] and historical_characters[k][i] then
					historical_characters[k][i].used = true
				end
			end
		end
	end
)