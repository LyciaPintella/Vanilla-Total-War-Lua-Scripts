	common.set_context_value(obedience_script_id, obedience_cco_table)
end

function eminence:raze_and_exterminate_details_for_region(region)
	if self:is_name_of_cult_centre_region(region:name()) then
		return self.config.raze_and_exterminate.cult_centre
	elseif region:is_province_capital() then
		return self.config.raze_and_exterminate.major_settlement
	else
		return self.config.raze_and_exterminate.minor_settlement
	end
end


function eminence:setup_garrisons_for_eminence()
	-- Attach eminence-related details to every garrison residence on the map indicating the settings for Raze and Exterminate and the award for normal Raze.
	-- Raze and Exterminate is applicable to settlements only, not ERSes.
	cm:iterate_regions(
		function(region)
			local region_garrison_residence = region:garrison_residence()
			local details = self:raze_and_exterminate_details_for_region(region)
			cm:set_script_state(region_garrison_residence, "eminence_from_raze", self.config.awards.raze_settlement)

			cm:set_script_state(region_garrison_residence, "eminence_for_raze_and_exterminate", details.cost)

			--cm:set_script_state(region_garrison_residence, "character_bundle_from_raze_and_exterminate", details.character_bundle_key)
			--cm:set_script_state(region_garrison_residence, "character_bundle_turns_from_raze_and_exterminate", details.character_bundle_turns)

			cm:set_script_state(region_garrison_residence, "force_bundle_from_raze_and_exterminate", details.force_bundle_key)
			cm:set_script_state(region_garrison_residence, "force_bundle_turns_from_raze_and_exterminate", details.force_bundle_turns)

			local slots = region:slot_list()
			for i = 0, slots:num_items() - 1 do
				cm:set_script_state(slots:item_at(i):garrison_residence(), "eminence_from_raze", self.config.awards.raze_ers)
			end
		end
	)
end

-- small util method to accumulate strings on new lines, without a leading new line
function eminence:message_append(message, addition)
	if message ~= "" then
		message = message .. "\n"
	end
	message = message .. addition
	return message
end

