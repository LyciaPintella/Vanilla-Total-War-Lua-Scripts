function eminence:setup_display_of_power_prebattle_button(display_of_power_button, local_faction, pending_battle)
	if not display_of_power_button then
		return
	end

	local is_eminence_unlocked = forge_path.is_system_unlocked_by_faction("eminence", local_faction:name())
	local opposing_character = self:get_opposing_character(local_faction, pending_battle)
	if not opposing_character then
		return false
	end
	local opposing_faction = opposing_character:faction()
	local is_opponent_human = opposing_faction:is_human()
	if is_opponent_human or
		not is_eminence_unlocked
	then
		display_of_power_button:SetVisible(false)
		return
	end

	display_of_power_button:SetVisible(true)
	local can_use, failure_reason = self:can_use_display_of_power(local_faction, pending_battle)
	display_of_power_button:SetState(can_use and "active" or "inactive")
	local display_of_power_cco_table = {}

	display_of_power_cco_table.IconPath = eminence.config.ui.display_power_icon

	display_of_power_cco_table.DisplayName = get_localised_random_string(eminence.config.ui.display_power_title_key)
	display_of_power_cco_table.Text = get_localised_random_string(eminence.config.ui.display_power_text_key)

	display_of_power_cco_table.EminenceCost = self.config.display_of_power.cost
	display_of_power_cco_table.EminenceCostString = tostring(display_of_power_cco_table.EminenceCost)
	
	local eminence_script_interface = local_faction:pooled_resource(self.config.resource.name)
	if eminence_script_interface then
		display_of_power_cco_table.EminenceOwned = eminence_script_interface:value() - pending_battle:bribed_units_eminence_cost_for_faction(local_faction:name())
		local eminence_max = eminence_script_interface:maximum_value()
		display_of_power_cco_table.EminenceMax = tostring(eminence_max)
	end
	display_of_power_cco_table.EffectBundleKey = self.config.display_of_power.effect_bundle

	display_of_power_cco_table.Flavor = get_localised_random_string(eminence.config.ui.display_power_flavor_key)

	display_of_power_cco_table.Warning = failure_reason
	-- we only show the action if there is no failure reason
	if failure_reason == "" then
		display_of_power_cco_table.Action = get_localised_random_string(eminence.config.ui.display_power_action_key)
	end

	local display_of_power_script_id = "display_of_power_script_id"
	display_of_power_button:SetProperty("script_id", display_of_power_script_id)
	common.set_context_value(display_of_power_script_id, display_of_power_cco_table)
end

