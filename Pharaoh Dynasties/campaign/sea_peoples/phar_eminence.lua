		else
			return pending_battle:attacker()
		end
	end
	return nil
end


-- Checks whether the Display of Power option should be enabled in the UI (it is always visible) and gives a reason if it should be disabled.
-- Return values: <option is enabled>, <reason for unavailability>
function eminence:can_use_display_of_power(faction, pending_battle)
	local result = ""
	-- If the opposing force already has the effect bundle, we cannot apply it again.
	local opposing_character = self:get_opposing_character(faction, pending_battle)
	if opposing_character and not opposing_character:is_null_interface() then
		local opposing_force = opposing_character:military_force()
		if opposing_force:has_effect_bundle(self.config.display_of_power.effect_bundle) then
			local localized_blocker = get_localised_random_string(eminence.config.ui.display_power_blocker_already_used_key)
			result = eminence:message_append(result, localized_blocker)
		end
	else
		script_error("ERROR: eminence:can_use_display_of_power received a battle with no opponent character.")
		return false, ""
	end

	-- Check whether the faction has enough Eminence to use Display of Power.
	-- Take into account the amount of Eminence currently blocked for bribing units.
	local cost = self.config.display_of_power.cost + pending_battle:bribed_units_eminence_cost_for_faction(faction:name())
	
	if not cm:faction_can_afford_resource_cost(faction, {{self.config.resource.name, cost }}) then
		local localized_blocker = get_localised_random_string(eminence.config.ui.display_power_blocker_cannot_afford_key)
		result = eminence:message_append(result, localized_blocker)
	end	

	if result ~= "" then
		return false, result
	end

	return true, ""
end

function eminence:perform_display_of_power(faction_cqi)
	local faction = cm:model():faction_for_command_queue_index(faction_cqi)
	local pending_battle = cm:model():pending_battle()

	if not self:can_use_display_of_power(faction, pending_battle) then
		return
	end

	-- Expend Eminence
	-- TODO: Later can replace the transaction with the line below if the cost stays eminence only.
	-- cm:faction_add_pooled_resource(faction:name(), self.config.resource.name, self.config.resource.pre_battle_factor, -self.config.display_of_power.cost)
	local cost = cm:create_new_custom_resource_cost()
	cost:add_resource_cost(self.config.resource.name, self.config.resource.pre_battle_factor, -self.config.display_of_power.cost)
	cm:faction_apply_resource_transaction(faction, cost)

	-- Apply an effect bundle to the enemy army
	local opposing_character = self:get_opposing_character(faction, pending_battle)
	if opposing_character then
		cm:apply_effect_bundle_to_characters_force(self.config.display_of_power.effect_bundle, opposing_character:command_queue_index(), 1)
	end

	local event_data =
	{
		faction_key = faction:name(),
	}
	core:trigger_event("ScriptEventEminenceDisplayOfPowerPerformed", event_data)
	cm:notify_display_of_power_used(faction_cqi)

	local local_faction = cm:get_local_faction(true)
	local local_faction_name = local_faction:name()

	if cm:get_campaign_ui_manager():is_panel_open("popup_pre_battle") then
		if local_faction_name == faction:name() then
			campaign_anouncement:display_announcement(eminence.config.ui.display_power_activation_setup)
		elseif local_faction_name == opposing_character:faction():name() then
			campaign_anouncement:display_announcement(eminence.config.ui.display_power_activation_reversed_setup)
		end
	end

	if local_faction_name == faction:name() and eminence.cache.display_of_power_button then
		eminence:display_eminence_buttons(core:get_ui_root())
	end
end

function eminence:remove_display_of_power_bundle(pending_battle)
	-- Called after a battle finishes. Remove any instances of the effect bundle applied by Display of Power before the battle.
	if pending_battle:has_attacker() then
		cm:remove_effect_bundle_from_characters_force(self.config.display_of_power.effect_bundle, pending_battle:attacker():command_queue_index())
	end
	if pending_battle:has_defender() then
		cm:remove_effect_bundle_from_characters_force(self.config.display_of_power.effect_bundle, pending_battle:defender():command_queue_index())
	end
end


function eminence:is_name_of_cult_centre_region(region_name)
	for _, god_data in ipairs(deities.gods) do
		if god_data.cult_centre_region_key == region_name then
			return true
		end
	end
	return false
end


function eminence:get_battle_settlement_type(settlement_battle)
	local garrison = settlement_battle:contested_garrison()
	if (not garrison) 
		or garrison:is_null_interface()
		or (not garrison:is_settlement())
	then
		local battle_type = settlement_battle:battle_type()
		script_error("ERROR: eminence:get_battle_settlement_type called for non-settlement (battle type '" .. battle_type .. "')")
		return nil
	end

	if self:is_name_of_cult_centre_region(garrison:region():name()) then
		return "cult_centre"
	elseif settlement_battle:is_minor_settlement_battle() then
		return "minor_settlement"
	elseif settlement_battle:is_major_settlement_battle() then
		return "major_settlement"
	end

	-- if none of the above are true, we can do a few more checks if the region is a capital
	-- this means its settlement is major
	if garrison:region():is_province_capital() then
		return "major_settlement"
	else
		return "minor_settlement"
	end
end


function eminence:obedience_cost(settlement_battle)
	-- Cost differs depending on the type of the settlement.
	local settlement_type = self:get_battle_settlement_type(settlement_battle)
	return self.config.obedience.cost[settlement_type]
end


-- Checks whether the Obedience option should be visible and enabled in the UI and gives a reason if the option is unavailable.
-- Return values: <option is visible>, <option is enabled>, <reason for unavailability>
function eminence:can_use_obedience(faction, pending_battle)
	local message = ""
	local option_visible = true
	local option_enabled = true
	local opposing_character = self:get_opposing_character(faction, pending_battle)
	if not opposing_character then
		return false
	end
	local opposing_faction = opposing_character:faction()
	local is_opponent_human = opposing_faction:is_human()
	-- The Obedience option is available only against an enemy settlement. Do not show it at all in non-settlement battles.
	local contested_garrison = pending_battle:contested_garrison()
	if contested_garrison:is_null_interface() 
		or contested_garrison:is_slot()
		or is_opponent_human
	then
		local localized_blocker = get_localised_random_string(eminence.config.ui.obedience_blocker_only_settlements_key)
		message = eminence:message_append(message, localized_blocker)
		option_visible = false
		option_enabled = false
		-- if the garrison is not valid there is no point in gathering all the other info
		return option_visible, option_enabled, message
	end
	local battle_type = pending_battle:battle_type()
	local player_character, is_from_attacking_side = cm:get_pending_battle_general_for_faction(faction, true)
	if battle_type == "fort_sally" or battle_type == "settlement_sally" or not is_from_attacking_side then
		option_visible = false
		option_enabled = false
		-- if this is a sally out or a defense, we don't show it and there is no point in gathering all the other info
		return option_visible, option_enabled, message
	end

	-- Cannot use Obedience against a faction capital.
	local contested_region = contested_garrison:region()
	local home_region = contested_garrison:faction():home_region()
	local is_battle_at_home_region = contested_region and home_region and not contested_region:is_null_interface() and not home_region:is_null_interface() and contested_region:name() == home_region:name()
	if not self.config.obedience.can_use_on_faction_capitals and is_battle_at_home_region then
		option_enabled = false
		local localized_blocker = get_localised_random_string(eminence.config.ui.obedience_blocker_not_capitals_key)
		message = eminence:message_append(message, localized_blocker)
	end
	
	-- Cannot use Obedience if the settlement has an army with too many units.
	if contested_garrison:has_army() and contested_garrison:army():unit_list():num_items() > self.config.obedience.max_enemy_units then
		option_enabled = false
		local localized_blocker = get_localised_random_string(eminence.config.ui.obedience_blocker_army_size_key)
		localized_blocker = common.string_safe_format(localized_blocker, self.config.obedience.max_enemy_units)
		message = eminence:message_append(message, localized_blocker)
	end

	-- Cannot use Obedience if the attacking army is itself spawned from an Obedience action.
	if pending_battle:attacker():military_force():force_type() == self.config.obedience.spawned_army_force_type then
		option_enabled = false
		local localized_blocker = get_localised_random_string(eminence.config.ui.obedience_blocker_not_self_key)
		message = eminence:message_append(message, localized_blocker)
	end

	-- Cost differs depending on settlement type. Take into account the amount of Eminence currently blocked for bribing units.
	local obedience_cost = self:obedience_cost(pending_battle)
	local bribed_units_cost = pending_battle:bribed_units_eminence_cost_for_faction(faction:name())
	local cost = obedience_cost + bribed_units_cost

	if not (cost and cm:faction_can_afford_resource_cost(faction, {{self.config.resource.name, cost}}) or false) then
		option_enabled = false
		local localized_blocker = get_localised_random_string(eminence.config.ui.obedience_blocker_cannot_afford_key)
		message = eminence:message_append(message, localized_blocker)
	end

	return option_visible, option_enabled, message
end


function eminence:perform_obedience(faction_cqi)
	local faction = cm:model():faction_for_command_queue_index(faction_cqi)
	local faction_name = faction:name()
	local pending_battle = cm:model():pending_battle()
	local region = pending_battle:region()
	local region_name = region:name()

	local option_visible, option_enabled, reason_for_unavailability = self:can_use_obedience(faction, pending_battle)
	if not (option_visible and option_enabled) then
		script_error("ERROR: eminence:perform_obedience called but Obedience should not be available (" .. reason_for_unavailability .. ")")
		return
	end

	-- Expend Eminence
	-- TODO: Later can replace the transaction with the line below if the cost stays eminence only.
	-- cm:faction_add_pooled_resource(faction:name(), self.config.resource.name, self.config.resource.pre_battle_factor, -self:obedience_cost(pending_battle))
	local cost = cm:create_new_custom_resource_cost()
	cost:add_resource_cost(self.config.resource.name, self.config.resource.pre_battle_factor, -self:obedience_cost(pending_battle))
	cm:faction_apply_resource_transaction(faction, cost)
	
	local local_faction = cm:get_local_faction(true)
	local local_faction_name = local_faction:name()
	if cm:get_campaign_ui_manager():is_panel_open("popup_pre_battle") then
		local opposing_character = self:get_opposing_character(faction, pending_battle)
		if local_faction_name == faction:name() then
			campaign_anouncement:display_announcement(eminence.config.ui.obedience_activation_setup)
		elseif local_faction_name == opposing_character:faction():name() then
			campaign_anouncement:display_announcement(eminence.config.ui.obedience_activation_reversed_setup)
		end
	end

	local attacking_character, _ = cm:get_pending_battle_general_for_faction(faction, false)

	local previous_owner = region:owning_faction()
	local previous_owner_cqi = is_faction(previous_owner) and previous_owner:command_queue_index() or 0

	-- Cancel the battle but first remember what the settlement was, we need that for generating the army below
	local garrison_residence = pending_battle:contested_garrison()
	local settlement_type = self:get_battle_settlement_type(pending_battle)
	cm:force_pending_battle_complete()

	-- Raze the settlement. Any enemy army garrisoned in it is automatically teleported just outside the border of the region.
	cm:set_region_abandoned(region_name)

	-- Spawn an army according to the type of settlement and the realm.
	local x, y = cm:find_valid_spawn_location_for_character_from_settlement(faction_name, region_name, false, true, 5)
	local realm_name = phar_realms.get_realm_for_region(region_name)
	local realm_army_templates = self.config.obedience.spawned_armies[realm_name] or self.config.obedience.spawned_armies.default
	cm:create_force_with_general(
		faction_name,
		realm_army_templates[settlement_type],
		region_name,
		x, y,
		"general",
		self.config.obedience.spawned_general_subtypes[faction_name],
		"", "", "", "", -- No names
		false,
		function(cqi)
			-- The "OBEDIENCE_ARMY" force type has the features "unable_to_exchange_units" and "general_is_irreplaceable".
			local character = cm:get_character_by_cqi(cqi)
			cm:convert_force_to_type(character, self.config.obedience.spawned_army_force_type)
			cm:apply_effect_bundle_to_characters_force(self.config.obedience.spawned_army_native_region_bundle, cqi, 0)
			local military_force = character:military_force()
			cm:set_script_state(military_force, "obedience_bundle_key", self.config.obedience.spawned_army_native_region_bundle)
			cm:set_script_state(military_force, "native_region", region_name)
			
			if pillars_civilization.is_nomad_or_sea_peoples_faction(character:faction():name()) then
				cm:apply_effect_bundle_to_characters_force("phar_sea_effect_bundle_horde_faction_obedience_army", cqi, 0)
			end

			-- when this army is removed, the supply points script will remove the supply score of this army
			-- but since it is spawned, not trained, it will not add the supply score
			-- therefore we need to add the supply manually, so after the army is spawned and destroyed the supply score of the faction remains as it was originally
			if is_supply_points_applicable(character:faction()) then
				local unit_list = military_force:unit_list()
				for i = 0, unit_list:num_items() - 1 do
					local unit = unit_list:item_at(i)
					-- commanders are handled in the supply_points_unit_created listener
					-- the normal units are not
					if not unit:has_unit_commander() then
						supply_points.update_supply_points_for_unit(unit, true, supply_points_factors.units)
					end
				end

				supply_points.apply_effects_for_faction(character:faction():name())
			end

			local event_data =
			{
				faction_key = faction_name,
				settlement_type = settlement_type,
				region_key = region_name,
				character = attacking_character,
			}
			core:trigger_event("ScriptEventEminenceObediencePerformed", event_data)
			cm:notify_obedience_used(faction_cqi, previous_owner_cqi, region:command_queue_index(), settlement_type)
		end
	)

	if local_faction_name == faction:name() and eminence.cache.obedience_button then
		eminence:display_eminence_buttons(core:get_ui_root())
	end
end


function eminence:update_obedience_armies_for_faction(faction)
	cm:iterate_forces(
		function(force)
			if force:force_type() == self.config.obedience.spawned_army_force_type then
				local general = force:general_character()
				local region = general and not general:is_null_interface() and general:region()
				local region_name = region and not region:is_null_interface() and region:name() or ""
				local realm_name = phar_realms.get_realm_for_region(region_name)
				local original_realm_name = phar_realms.get_realm_for_region(cm:get_string_script_state(force, "native_region"))
				if realm_name == original_realm_name then
					cm:apply_effect_bundle_to_force(self.config.obedience.spawned_army_native_region_bundle, force:command_queue_index(), 0)
				else
					cm:remove_effect_bundle_from_force(self.config.obedience.spawned_army_native_region_bundle, force:command_queue_index())
				end
			end
		end,
		faction)
end

function eminence:handle_popup_pre_battle_open(component)
	local pending_battle = cm:model():pending_battle()
	if (not pending_battle) or
		(not pending_battle:has_attacker())
	then
		return
	end

	local local_faction = cm:get_local_faction(true)
	local player_character, _ = cm:get_pending_battle_general_for_faction(local_faction, false)
	if not player_character then
		-- this can happen in MP if the game settings allow one player to fight on the side of the AI vs the other player
		return
	end

	eminence.cache.display_of_power_button = find_uicomponent(component, "button_display_of_power")
	eminence.cache.obedience_button = find_uicomponent(component, "button_obedience")
	eminence.is_executing.Eminence_PreBattle_DisplayOfPower = false
	eminence.is_executing.Eminence_PreBattle_Obedience = false

	eminence:display_eminence_buttons(component)
end

